package Primitives.Numerical;

public class ShortType {
    // short
    // Size: 16 bit
    // range of values it can store: -32K to +32K
    // Use: Early computing efficient storage for small numerical values
    public static void main(String[]args){

        // creation of short data type
        short number = 10388;

        // To print the value of the variable we call the variable with its name.
        System.out.println("The value of the variable number is " + number);

        number = 10 ; // the data type still remains as short

        System.out.println("The new value of the variable number " +  10);

    }


}
