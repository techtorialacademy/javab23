package StringObject.StringMethods;

public class _5_ToLowerUperMethod {
    // ToLowerCase Method
    // Returns: Another String
    // Purpose: Gives the all letters in string in lower case
    // Syntax
    // str1.toLowerCase();
    // Original value of str1 will be protected.
    // ToUpperCase Method
    // Returns: Another String
    // Purpose: Gives the all letters in string in upper case
    // Syntax
    // str1.toUpperCase();
    // Original value of str1 will be protected.

    public static void main(String[]args){

        String s1 = "JaVa";

        System.out.println(s1.toUpperCase()); // JAVA
        System.out.println(s1.toLowerCase()); // java

        System.out.println(s1); // JaVa

        // No method could change the original value of the string object.












    }








}
