package StringObject.StringMethods;

public class _3_CharAtMethod {
    // CharAt Method
    // Returns: char value
    // Purpose: finds the character located at given index
    // Syntax
    // str1.charAt(indexNumber);
    // the method above gives the char value located at given indexNumber.

    public static void main(String [] a){
        //           012345
        String s1 = "python";

        // I want to learn character located at index number 4
        System.out.println(s1.charAt(4)); // o

        // I want to learn 3rd character in string object s1
        // Third character is located at index number 2
        char thirdCharacter = s1.charAt(2);

        System.out.println(thirdCharacter);







    }




}
